#Again, first we import the turtle module.
import turtle
#turtle working
slowpoke = turtle.Turtle()
#shape of turtle
slowpoke.shape('turtle')
#turtle to go forward 100 steps
slowpoke.forward(100)
#right function to have it turn right 90 degrees
slowpoke.right(90)
slowpoke.forward(100)
slowpoke.right(90)
slowpoke.forward(100)
slowpoke.right(90)
slowpoke.forward(100)
slowpoke.right(90)

turtle.mainloop()
